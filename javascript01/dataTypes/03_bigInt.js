/* BigInt and regular numbers must be explicitly converted using either BigInt() or Number(), as shown below:  
*/
//Example:
let bigintvar = 6n;
let numvar = 3;

// number to bigint 
alert(bigintvar + BigInt(numvar));// 9

// bigint to number
alert(Number(bigintvar )+ numvar); //9

